    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        try {
            classAPI cls = new classAPI();

            String url = "http://localhost:8080/html/TranVanTruong_22647761/TranVanTruong_taoapi/them.php?"
                    + "soluong=" + txtsoluong.getText()
                    + "&ngaymuahang=" + txtngaymuahang.getText()
                    + "&idkh=3";

            cls.goiapi(url);
            loaddulieu();
            xoatext();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_themActionPerformed

    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        int row = jtable.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Chọn dòng!");
            return;
        }

        try {
            classAPI cls = new classAPI();

            String id = jtable.getValueAt(row, 0).toString();

            String url = "http://localhost:8080/html/TranVanTruong_22647761/TranVanTruong_taoapi/xoa.php?idhd=" + id;

            cls.goiapi(url);
            loaddulieu();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_xoaActionPerformed

    private void jtableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtableMouseClicked
        int row = jtable.getSelectedRow();

        if (row != -1) {
            txtidhd.setText(jtable.getValueAt(row, 0).toString());
            txtsoluong.setText(jtable.getValueAt(row, 1).toString());
            txtngaymuahang.setText(jtable.getValueAt(row, 2).toString());
        }
    }//GEN-LAST:event_jtableMouseClicked
    
    
    public void xoatext(){
        txtsoluong.setText("");
        txtngaymuahang.setText("");
    }
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
