    private void loaddulieu() {
        try {
            classAPI cls = new classAPI();
            JSONArray arr = cls.docapi();

            Vector<Vector<String>> data = new Vector<>();
            Vector<String> title = new Vector<>();

            title.add("ID hoa don");
            title.add("So luong");
            title.add("Ngay mua hang");
            title.add("Ho dem");
            title.add("Ten");

            for (int i = 0; i < arr.length(); i++) {
                JSONObject o = arr.getJSONObject(i);

                Vector<String> row = new Vector<>();
                row.add(o.getString("idhd"));
                row.add(o.getString("soluong"));
                row.add(o.getString("ngaymuahang"));
                row.add(o.getString("hodem"));
                row.add(o.getString("ten"));

                data.add(row);
            }

            jtable.setModel(new DefaultTableModel(data, title));

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
